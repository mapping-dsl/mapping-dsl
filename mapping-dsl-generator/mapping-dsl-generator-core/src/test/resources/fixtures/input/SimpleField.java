package pojo;

import java.util.List;

public class SimpleField {

    private String stringValue;

    private int intValue;

    private Integer anotherIntegerValue;

    private boolean booleanValue;

    private Boolean anotherBooleanValue;

    private List<String> listOfString;

    private String[] arrayOfString;

    public String getStringValue() {
        return stringValue;
    }

    public void setStringValue(String stringValue) {
        this.stringValue = stringValue;
    }

    public int getIntValue() {
        return intValue;
    }

    public void setIntValue(int intValue) {
        this.intValue = intValue;
    }

    public Integer getAnotherIntegerValue() {
        return anotherIntegerValue;
    }

    public void setAnotherIntegerValue(Integer anotherIntegerValue) {
        this.anotherIntegerValue = anotherIntegerValue;
    }

    public boolean isBooleanValue() {
        return booleanValue;
    }

    public void setBooleanValue(boolean booleanValue) {
        this.booleanValue = booleanValue;
    }

    public Boolean getAnotherBooleanValue() {
        return anotherBooleanValue;
    }

    public void setAnotherBooleanValue(Boolean anotherBooleanValue) {
        this.anotherBooleanValue = anotherBooleanValue;
    }

    public List<String> getListOfString() {
        return listOfString;
    }

    public void setListOfString(List<String> listOfString) {
        this.listOfString = listOfString;
    }

    public String[] getArrayOfString() {
        return arrayOfString;
    }

    public void setArrayOfString(String[] arrayOfString) {
        this.arrayOfString = arrayOfString;
    }

}
